sayi1 = input("Lütfen birinci sayıyı giriniz: ")
sayi2 = input("Lütfen ikinci sayıyı giriniz: ")
sayi3 = input("Lütfen üçüncü sayıyı giriniz: ")
ortalama = (float(sayi1) + float(sayi2) + float(sayi3)) / 3
print(f"Girdiğiniz sayıların ortalaması: {ortalama}")
print(f"ortalama (2ondalık): {ortalama:.2f}")